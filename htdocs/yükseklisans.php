                <h1>DEĞERLENDİRME</h1>
                <p>
                    <b>Mülakat Sınavsız Değerlendirme Koşulları;</b><br />
                    1. Adayların değerlendirilmeleri yapılırken ALES puanının %60’ı, Lisans mezuniyet notunun
                    %30’u ve varsa Yabancı Dil puanının %10’unun toplamı ile elde edilecek Başarı Notu,
                    en az 60 (altmış) olmak şartı ile en yüksek puan alan adaydan başlamak üzere sıralanarak asil
                    ve yedek aday listeleri belirlenir.<br /><br />
                    <b> Mülakat Sınavlı Değerlendirme Koşulları;</b> <br />
                    1. Başvuruda bulunan adaylardan mülakat sınavına alınacakların sayısı, ilan edilen kontenjanın 3
                    (üç) katı ile sınırlandırılır. Adayların değerlendirilmeleri yapılırken ALES (veya
                    Üniversitelerarası Kurul (veya YÖK) tarafından ALES’e eş değerliği kabul edilen sınavın eş
                    değer) puanının %60’ı, Lisans mezuniyet notunun %30’u ve varsa Yabancı Dil puanının
                    %10’unun toplam sonucu, en yüksek puan alan adaydan başlamak üzere adaylar ilgili
                    puanlarına göre sıralanarak, mülakat sınavına alınacak adaylar belirlenir.<br /> <br />
                    2. Adaylar, EYK kararı ile belirlenen en az 3 (üç) en fazla 5 (beş) asıl ve 2 (iki) yedek üyeden
                    oluşan jüri tarafından mülakat sınavına tabi tutulur. Mülakat sınavı, yazılı ve/veya sözlü
                    yapılabilir.<br /> <br />
                    3. Mülakat sınavına alınan adayların Başarı Notu , ALES (veya Üniversitelerarası Kurul (veya
                    YÖK) tarafından ALES’e eş değerliği kabul edilen sınavın eş değer) puanının %50’si ile
                    lisans mezuniyet notunun %20’si, varsa Yabancı Dil puanının %10’u ve mülakat sınavının
                    %20’si alınarak hesaplanır. Başarı notunun virgülden sonra 3 (üç) hanesi dikkate alınır.
                    <br /> <br />
                    4. Adayların m ü l a k a t sınavlı/mülakat sınavsız değerlendirme sonucunda Başarı Notları, en
                    az 60 (altmış) olmak şartı ile en yüksek puan alan adaydan başlamak üzere sıralanır,
                    adaylar başvuruda bulundukları programlara, ilan edilen kontenjanlarına göre yerleştirilir.
                    Eşit başarı notuna sahip olan adaylar arasında önceliğin belirlenmesinde; sırasıyla ALES
                    (veya Üniversitelerarası Kurul (veya YÖK) tarafından ALES’e eş değerliği kabul edilen
                    sınavın eş değer) puanı, Lisans mezuniyet notu ve varsa Yabancı Dil puanı dikkate alınır.<br /> <br />
                    5. Mülakat sınavlı değerlendirme ile öğrenci alımı yapan anabilim dallarında mülakat sınavına
                    girmeyen veya mülakat sınav puanı 60 (altmış)’ın altında olan adaylar, değerlendirmeye
                    alınmaz. Puanlar büyükten küçüğe doğru sıralanarak enstitünün internet adresinde duyurulur.
                    <br /> <br />
                    6. Enstitü internet sitesinden yapılmış olan duyurular adaylara yönelik tebligat niteliğindedir.
                    İlgili başvuru takvimi içerisinde internet sitesinden yapılan duyurulara rağmen kayıt için
                    müracat etmeyen adaylar mülakat sonucu kazanmış oldukları kayıt haklarını yitireceklerdir.
                    <br /> <br />
                    7. Herhangi bir lisansüstü programa kabul edilebilecek öğrenci sayısı 3(üç)’ten az ise, ilgili EYK
                    o yarıyıl için, söz konusu lisansüstü programa öğrenci alınmamasına karar verebilir. İkinci
                    öğretim lisansüstü programlarının açılması için bu sınır EYK tarafından belirlenir.<br /> <br />
                    8. Tezsiz Yüksek Lisans II.Öğretim programlarında başarı sıralaması Lisans mezuniyet notuna
                    göre yapılacaktır.
