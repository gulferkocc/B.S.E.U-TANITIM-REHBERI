                <h1> Erasmus Değişim Programı</h1>
                <p>
                    Erasmus, üniversite ve yüksek lisans öğrencilerinin eğitimlerine Avrupa Birliği (AB) üyesi bir ülkedeki anlaşmalı bir yükseköğretim kurumunda devam etmelerini sağlayan bir öğrenci değişim programıdır. Öğrencilerimiz, Üniversitemizin ortağı olduğu Erasmus programı ile eğitimlerini bir bölümünü yurt dışında görebilme imkânına sahiptir. Üniversitemiz, AB eğitim programlarına üst düzeyde önem veren, Bologna süreciyle uyumlu, uluslararası bir üniversite hedefinde her geçen gün gelişen bir yapıdadır. Üniversitemizin, Erasmus programı kapsamında; Belçika, Bulgaristan, Çek Cumhuriyeti, Hırvatistan, İspanya, İtalya, Letonya, Litvanya, Macaristan, Makedonya, Polonya, Portekiz, Romanya, Slovakya ve Slovenya’dan çeşitli üniversitelerle toplam 133 ikili anlaşması bulunmaktadır.
                </p>
                <h1>Farabi Değişim Programı</h1>
                <p>Kısaca “Farabi Değişim Programı” olarak adlandırılan “Yükseköğretim Kurumları Arasında Öğrenci ve Öğretim Üyesi Değişim Programı” ile yurt içinde eğitim veren yükseköğretim kurumları arasında ön lisans, lisans, yüksek lisans ve doktora öğrencilerinin değişimi sağlanmaktadır. Her öğrenci bir defaya mahsus olmak üzere, 1 veya 2 yarıyıl için Farabi Değişim Programı’ndan faydalanabilir. Üniversitemizin Farabi Programı kapsamında ülkemizin 76 farklı üniversitesi ile ikili anlaşması bulunmaktadır.</p>


