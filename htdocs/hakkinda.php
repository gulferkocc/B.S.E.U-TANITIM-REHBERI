                <h1>Yatay Geçiş </h1>

                <h3>KOŞULLAR</h3>

                <div>
                    1. Kurumlar arası yatay geçiş yükseköğretim kurumlarının aynı düzeydeki eş değer (İsimleri aynı olan veya
                    ilgili yönetim kurulları tarafından içeriklerinin en az yüzde sekseni aynı olduğu tespit edilen) diploma programları
                    arasında ve Yükseköğretim Kurulu tarafından yayımlanan kontenjanlar çerçevesinde yapılır. Ön lisans diploma
                    programlarının ilk yarıyılı ile son yarıyılına, lisans diploma programlarının ilk iki yarıyılı ile son iki yarıyılına
                    yatay geçiş yapılamaz.
                </div>
                <br />
                <div>
                    2. Açık veya uzaktan öğretimden diğer açık veya uzaktan öğretim diploma programlarına yatay geçiş
                    yapılabilir. Açık ve uzaktan öğretimden örgün öğretim programlarına geçiş yapılabilmesi için, öğrencinin öğrenim
                    görmekte olduğu programdaki genel not ortalamasının 100 üzerinden 80 veya üzeri olması veya kayıt olduğu
                    yıldaki merkezi yerleştirme puanının, geçmek istediği üniversitenin diploma programının o yılki taban puanına eşit
                    veya yüksek olması gerekir. Ayrıca öğrencilerin açık ve uzaktan eğitim programlarından alıp başardığı derslerden
                    sadece Atatürk İlkeleri ve İnkılap Tarihi, Türk Dili ve İngilizce derslerinin muafiyet ve intibak işlemleri yapılır.
                </div>
                <br />
                <div>
                    3. Kurum içi ve kurumlar arası yatay geçiş yapmak isteyen öğrencilerin değerlendirme puanı hesaplanırken
                    merkezi yerleştirme puanının %80’i, yüzlük sistemdeki genel not ortalamasının %20’si alınarak sıralama yapılır,
                    buna göre asıl ve yedek adaylar belirlenir. Koşulları sağlayan başvuruların, açılan kontenjan sayısını geçmesi
                    durumunda değerlendirme puanına göre sıralama yapılacaktır.
                </div>
                <br />
                <div>
                    4. Kayıt dondurmuş olmak, yatay geçiş hakkından yararlanmak için engel teşkil etmez.

                </div>
                <br />
                <div>
                    5. Başvuru için öğrencinin ayrılacağı kurumun ders planlarındaki geçiş yapmak istediği döneme kadar
                    bütün derslerini almış ve sınavlarını başarmış olması gerekir.
                </div>
                <br />
                <div>
                    6. Kurumlar arası yatay geçiş için öğrencinin, kayıtlı olduğu programda bitirmiş olduğu dönemlere ait
                    genel not ortalamasının 100 tam not üzerinden en az 60 veya 4 tam not üzerinden en az 2,29 olması gerekir. Bu
                    başarı şartını sağlayamayan ancak merkezi yerleştirme puanı geçiş yapmak istediği diploma programının taban
                    puanına eşit veya yüksek olan adaylar yatay geçiş başvurusu yapabilir. Kurum içi yatay geçiş başvurusu yapacak
                    adayların kayıtlı olduğu programda bitirmiş olduğu dönemlere ait genel not ortalamasının 4 tam not üzerinden en
                    az 2.00 olması gerekir.
                </div>
                <br />
                <div>
                    7. İkinci öğretimden sadece ikinci öğretime yatay geçiş yapılabilir. Ancak, ikinci öğretim diploma
                    programlarında bulunduğu sınıfın ilk yüzde onuna girerek bir üst sınıfa geçen öğrenciler birinci öğretim diploma
                    programına kontenjan dâhilinde yatay geçiş yapabilirler. Bu yatay geçişler de örgün öğretimde uygulanan mevzuat
                    hükümlerine tabidir.

                </div>
                <br />
                <div>
                    8. Dörtlük veya yüzlük sisteme göre elde edilen başarı notlarının birbirine dönüştürülmesinde,
                    Yükseköğretim Yürütme Kurulu tarafından belirlenen dönüştürme tabloları kullanılır.
                </div>
                <br />
                <div>
                    9. Kurum içi yatay geçişlerde öğrenciler Meslek Yüksekokullarından kendi programlarının diğer
                    öğretimlerine ve diğer birimlerin sadece aynı isimdeki programlarına geçiş yapabilir. Fakültelerin öğrencileri ise
                    kendi programlarının diğer öğretimlerine kurum içi yatay geçiş kontenjanından başvurabilir.
                </div>
                <br />
                <div>
                    10. Yeterli şartları taşıyan aday olması halinde aynı sayıda asıl ve yedek aday belirlenir. Takvimde
                    belirlenen süre içinde kesin kayıt için başvurmayan asıl adayların yerine yedeklerin başvurusu alınır.
                </div>
                <br />
                <div>
                    11. Yurt dışı üniversitelerden yatay geçiş aynı düzeydeki eş değer (İsimleri aynı olan veya ilgili yönetim
                    kurulları tarafından içeriklerinin en az yüzde sekseni aynı olduğu tespit edilen) diploma programlarına
                    Yükseköğretim Kurulu tarafından yayımlanan kontenjanlar çerçevesinde yapılır. Bu kapsamda başvuran
