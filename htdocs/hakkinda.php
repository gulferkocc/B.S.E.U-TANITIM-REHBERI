                    kayıt yaptırmış olan öğrencilere; yerleştiği lisans programı tercihleri arasında ilk 15 tercihinin içinde yer alması, önceden yurt
                    içinde veya yurt dışında herhangi bir lisans derecesine sahip olmaması, zorunlu hazırlık sınıfı ve lisans öğrenim süresi
                    (4 yıl, çift ana dal programında kayıtlı öğrencilere ise en fazla 5 yıl) boyunca ağırlıklı genel not ortalamasının yıllık olarak
