                <h3>Engelsiz Üniversite</h3>
                <p>
                    Engelli Öğrenciler Birim Koordinatörlüğümüz; tüm öğrencilerimizin eşit eğitim hakkından faydalanabilmeleri, sosyo-kültürel faaliyetlere katılabilmeleri ve üniversitemiz içerisinde rahat mekânsal erişim sağlayabilmeleri amacıyla ihtiyaç duyulan altyapı çalışmalarını titizlikle yürütmektedir. Bu amaçla, özel gereksinimli öğrencilerimizin tüm kampüslerimizde rahat ulaşım sağlayabilmeleri için rampalar, asansörler, hissedilebilir yürüme yüzeyleri ve bina girişlerinde özel giriş sistemleri kurulmuştur. 2018 yılından itibaren, Yükseköğretim Kurulu (YÖK) tarafından düzenlenen Engelsiz Üniversite Ödülleri’nin “Mekânda Erişilebilirlik” kategorisinde her yıl ödüle layık görülen Üniversitemiz, 2020 yılında 8 Turuncu Bayrak ödülü alarak, 116 üniversite arasında 2’nci sırada yer alma başarısı göstermiştir. Kütüphanemizin özel gereksinimli öğrenciler için hazırladığı okuma/çalışma odasına, görme engelli öğrencilerimizin daha rahat çalışabilmeleri için e-bot cihazı kurulmuştur. Sosyo-kültürel faaliyetler kapsamında, 2020 yılında Goalball karşılaşması ve tüm engelli öğrencilerimizin katılımına açık olan ücretsiz atletizm çalışmaları yapılmış olup, çeşitli sergiler düzenlenmiştir. Öğrencilerimizin ihtiyaç duyması durumunda; tek başına sınava girme, gözetmen desteği ve sınav sorularının özel hazırlanması gibi planlamalar yapılmaktadır. Üniversitemizin; Rektörlük, Kütüphane, Fen Edebiyat Fakültesi, Güzel Sanatlar Fakültesi, İktisadi ve İdari Bilimler Fakültesi, İslami ilimler Fakültesi, Mühendislik Fakültesi, Sağlık Bilimleri Fakültesi, Uygulamalı Bilimler Fakültesi, Ziraat Fakültesi, Osmaneli Meslek Yüksekokulu, Pazaryeri Meslek Yüksekokulu, Sağlık Hizmetleri Meslek Yüksekokulu ve Söğüt Meslek Yüksekokulu, “Mekânda Erişilebilirlik” dalında Turuncu Bayrak sahibidir.
